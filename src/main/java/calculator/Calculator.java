package calculator;

public class Calculator {
    // (ctrl + shift + t)  테스트 클래스 생성
    public static int add(int i, int j) {
        return i + j;
    }

    public static int minus(int i, int j) {
        return i - j;
    }

    public static int  multiply(int i, int j) {
        return i * j;
    }
}
